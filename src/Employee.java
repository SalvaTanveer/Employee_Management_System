
public class Employee {
	String id;
	String name;
	int salary;
	String email;
	String phone;
	String address;
	public Employee(String id, String name, int salary, String email, String phone, String address) {
		super();
		this.id = id;
		this.name = name;
		this.salary = salary;
		this.email = email;
		this.phone = phone;
		this.address = address;
	}
}
